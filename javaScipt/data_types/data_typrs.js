//string

let employeeName = "john";
console.log(employeeName);

// for know data type----write 'typeof'

let EmployeeName = "john";
console.log(typeof EmployeeName);

let EmployeeName1 = "john";
console.log(`type: ${typeof EmployeeName1}, value: ${EmployeeName1}`);


// number

let employeeAge = 30;
console.log(`value: ${employeeAge}, type ${typeof employeeAge}`);

// boolean

let isManager = "false";
console.log(`value : ${isManager}, type : ${typeof isManager}`);

let isEmployee = "true";
console.log(`value : ${isEmployee}, type : ${typeof isEmployee}`);


//undefine

let name;
console.log(name);
name = "Raju";

let name1;
console.log(name1);

//null

let test = "n/a";
